Aujourd\'hui nous allons aborder l\'aspect einteinien du temps et de l\'espace
Savais-tu que chaque fois qu\'on parle de gravité tout le monde sur toute la terre commet une grave erreur ? Einstein a pourtant résolu cela.
Quand il tire la langue c\'est pour dire : \"le temps est relatif, l\'espace est relatif, tout est relatif\".
Le temps est la différence d\'information entre deux moments donnés et dont les états sont mémorisés. Il ne s\'évalue donc qu\'en dehors de ce segment, comme une simple soustraction.
C\'est pourquoi selon que la densité neuronale est grande ou petite selon ce qu\'on pense, le temps paraît s\'écouler plus ou moins vite : c\'est une question de quantité d\'information. Sans information, point de temps.

L\'espace est la quantité de lignes d\'espace qu\'il faut traverser pour aller du point A au point B. L\'astuce avec l\'espace est que la courbure de l\'espace détermine la quantité d\'espace qu\'il y a à cet endroit.
Bon ok je la refais : Einstein s\'est dit, en comparant l\'accélération dans un train à la gravité, puisqu\'\'une fois le train lancé l\'accélération est nulle, alors pourquoi à la surface de la terre l\'accélération est positive ? Il en a conclu que ce n\'est pas l\'objet qui tombe comme attiré par une masse (sur ce point il a raison et tout le monde a tort), mais le sol qui accélère vers le haut indéfiniment à une vitesse logarithmique. Étonnant non ?
En fait ce n\'est qu\'un expérience mentale, mais tout simplement, c\'est ce que j\'ai dit juste avant : la masse courbe l\'espace. Ou plutôt en fait la masse est une courbure de l\'espace, comme un doigt sous un drap [le drap est l\'espace, la bosse est une masse]. Les fils du drap sont les lignes d\'espace. Contrairement au drap (l\'espace 3d est une antisphère) les lignes d\'espace sont plus denses à l\'approche d\'une masse ; et donc l\'espace d\'autant plus courbé. Ce n\'est pas nous qui accélérons vers le haut, mais les liges d\'espace qui sont sans cesses projetées vers le bas. C\'est cela qui fait la gravité.

